
# ------------------------------------------------------------------------------------------
# ## 上手Jupyter notebook
#
# ### 执行一个cell
# 要执行一个cell中的代码, 选中那个cell然后要么(1) 按 `Shift` + `Enter` ，要么(2)点击上面的运行按钮（Run）。【译注：(3)也可以使用`Ctrl`
# + `Enter`运行当前单元】
# ------------------------------------------------------------------------------------------

1 + 1
2 + 2

# ------------------------------------------------------------------------------------------
# 要注意的是：执行一个cell时只有最后一行会默认打印，想要抑制这个打印行为在行结束时加上分号`;` 就行了，就像下面这样
# ------------------------------------------------------------------------------------------

1 + 1
2 + 2;

# ------------------------------------------------------------------------------------------
# ### 如何查看Julia函数的帮助文档
#
# 查看不熟悉的Julia函数的帮助文档，在函数名前面加个问号就行。（在Julia的REPL中也可以哦！）
# ------------------------------------------------------------------------------------------

?println

# ------------------------------------------------------------------------------------------
# ### 如何使用shell命令
#
# 在shell命令前加上分号 `;` 就行，像这样：
# ------------------------------------------------------------------------------------------

;ls

;pwd

# ------------------------------------------------------------------------------------------
# shell命令在Julia的REPL中也可以使用哦！
#
# 【译注：Windows系统下得使用Windows命令行命令，像这样：】
# ------------------------------------------------------------------------------------------

;dir
