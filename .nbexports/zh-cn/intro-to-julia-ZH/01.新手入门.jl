# #### 1.2
# 给变量`days`赋值`365`，将`days`转为浮点数（float）并将其赋值给变量`days_float`
# ------------------------------------------------------------------------------------------

days = 365
days_float = convert(Float64, days)

@assert days == 365
@assert days_float == 365.0


