# ------------------------------------------------------------------------------------------
# ## 如何给变量赋值
#
# 只需要变量名、变量值和一个等号！<br>
# Julia会自动判断类型。
# ------------------------------------------------------------------------------------------

my_answer = 42
typeof(my_answer)

my_pi = 3.14159
typeof(my_pi)

😺 = "开心的猫!"
typeof(😺)

# ------------------------------------------------------------------------------------------
# 要打出“开心的猫”，用tab键补全选择emoji的名字然后再按tab键即可
# ------------------------------------------------------------------------------------------

# \:smi + <tab> --> 上下键选定 + <enter> ---> <tab> + <enter> ---> 完成
# 即\:smile_cat: + <tab>

# ------------------------------------------------------------------------------------------
# 在给一个变量赋值后，可以给它重新赋任意类型的值而不会报错。
# ------------------------------------------------------------------------------------------

😺 = 1

typeof(😺)

# ------------------------------------------------------------------------------------------
# 注意：Julia允许用超广泛的字符，就比如😺
#
# 这就允许我们可以把代码写成这样：
# ------------------------------------------------------------------------------------------

😀 = 0
😞 = -1

😺 + 😞 == 😀

