# ------------------------------------------------------------------------------------------
# ## Comenzando con Jupyter Notebooks
#
# Un documento de **Jupyter** está compuesto por celdas. Las celdas se delimitan por cajas
# con bordes grises o verdes, dependiendo si se están editando o no.
#
# Las celdas se clasifican en dos tipos: celdas de código y de texto tipo *markdown*.
#
# ### Editando celdas tipo *Markdown*
# Para ver el texto plano de esta celda, solamente debes hacer doble click sobre el texto.
# Automáticamente podrás editar el texto y caerás en cuenta lo sencillo es darle formato.
#
# Aquí encontrarás un [tutorial](https://www.markdowntutorial.com) y [manual de referencias
# rápido](https://en.support.wordpress.com/markdown-quick-reference/) con la información
# básica que vas a necesitar para darle un formato pulcro a tus archivos.
#
# ### Corriendo una celda de código
# Para ejecutar una celda de código, seleccióna la celda y (1) presiona `Shift` + `Enter` o
# (2) haz click en el botón de correr que se encuentra en la barra de herramientas superior:
# ------------------------------------------------------------------------------------------

1 + 1
2 + 2

# ------------------------------------------------------------------------------------------
# Si eres buen observador podrás notar que solamente la última línea de cada celda se
# imprime a pantalla al ejecutarse. Se puede suprimir esta impresión a pantalla con un
# semicolon.
# ------------------------------------------------------------------------------------------

1 + 1
2 + 2;

# ------------------------------------------------------------------------------------------
# ### ¿Cómo buscar información sobre las funciones?
#
# ¡Igualito que en la REPL!
# 
# ------------------------------------------------------------------------------------------

?println

# ------------------------------------------------------------------------------------------
# ### ¿Cómo usar comandos del shell?
